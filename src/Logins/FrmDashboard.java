    private void btnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportActionPerformed
        // TODO add your handling code here:
        try {
            JFileChooser jFileChooser = new JFileChooser();
            jFileChooser.showSaveDialog(this);
            File saveFile = jFileChooser.getSelectedFile();

            if (saveFile != null) {
                saveFile = new File(saveFile.toString() + ".xlsx");
                Workbook wb = new XSSFWorkbook();
                Sheet sheet = wb.createSheet("DNPC");

                org.apache.poi.ss.usermodel.Row rowCol = sheet.createRow(0);
                for (int i = 0; i < tblrecordgenerated.getColumnCount(); i++) {
                    org.apache.poi.ss.usermodel.Cell cell = rowCol.createCell(i);
                    cell.setCellValue(tblrecordgenerated.getColumnName(i));
                }

                for (int j = 0; j < tblrecordgenerated.getRowCount(); j++) {
                    org.apache.poi.ss.usermodel.Row row = sheet.createRow(j + 1);
                    for (int k = 0; k < tblrecordgenerated.getColumnCount(); k++) {
                        org.apache.poi.ss.usermodel.Cell cell = row.createCell(k);
                        if (tblrecordgenerated.getValueAt(j, k) != null) {
                            cell.setCellValue(tblrecordgenerated.getValueAt(j, k).toString());
                        }
                    }
                }
                FileOutputStream out = new FileOutputStream(new File(saveFile.toString()));
                wb.write(out);
                wb.close();
                out.close();
                openFile(saveFile.toString());
            } else {
                JOptionPane.showMessageDialog(null, "Error in Export");
            }
        } catch (FileNotFoundException e) {
            System.out.println(e);
        } catch (IOException io) {
            System.out.println(io);
        }

    }//GEN-LAST:event_btnExportActionPerformed

