    public void getStudRecord() {
        DefaultTableModel model = (DefaultTableModel) tblVewAllRecord.getModel();
        for (int i = model.getRowCount() - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        c.Select("SELECT * FROM `student_reg`");
        try {
            while (c.rs.next()) {
                Object[] row = {c.rs.getString("grno"), c.rs.getString("student_name"), c.rs.getString("email_id"), c.rs.getString("gender"), c.rs.getString("dob"), c.rs.getString("mob_no"), c.rs.getString("address"), c.rs.getString("city"), c.rs.getString("district"), c.rs.getString("state"), c.rs.getString("dept"), c.rs.getString("sem"), c.rs.getString("year")};
                model.addRow(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmDashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

